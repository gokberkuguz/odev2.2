public class Category {
    private int id;
    private String categoryTuru;
    private int toplamKurs;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCategoryTuru() {
        return categoryTuru;
    }

    public void setCategoryTuru(String categoryTuru) {
        this.categoryTuru = categoryTuru;
    }

    public int getToplamKurs() {
        return toplamKurs;
    }

    public void setToplamKurs(int toplamKurs) {
        this.toplamKurs = toplamKurs;
    }
}
